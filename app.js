
// Print the odd number from 1 to 100

for(let i = 1; i <= 100; i++ ){
  if(i % 2 == 0)
  continue;
  document.write(" "+ i)
}
document.write( ' <br> End the odd Number. <br> ')

// Print the even number from 1 to  1000

for (let e = 0; e <= 100; e++ ) {
  if (e % 2 !=0 ) {
    continue;
  }
  document.write(" " + e)
}